package bean;

public class LoginBean
{

	private int		UserID;
	private String	Password;

	public int getUserID()
	{

		return UserID;
	}

	public void setUserID( int UserID )
	{

		this.UserID = UserID;
	}

	public String getPassword()
	{

		return Password;
	}

	public void setPassword( String Password )
	{

		this.Password = Password;
	}

}